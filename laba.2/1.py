#задание 3-4
#Создайте два двумерных массива NumPy размером (3, 3) со случайными значениями.
#Перемножьте два созданных двумерных массива и выведите результат на экран.
def f_3_4():
    N = 3
    arr_1 = np.random.randint(-1000, 1000, size=(N, N))
