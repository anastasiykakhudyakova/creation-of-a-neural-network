#задание 5
#Создайте одномерный массив NumPy из 10 случайных целых чисел. 
#Примечание: для этого Вам понадобится изучить функцию np.random.randint().
def f_5():
    N = 10
    arr = np.random.randint(-1000, 1000, size=N)
    return arr

#задание 6
#Используйте индексацию и срезы, 
#чтобы вывести только чётные элементы из массива, созданного в предыдущем задании.
def f_6():
    arr = f_5()
    print(arr[arr % 2 == 0])
    
#задание 7  
#Выведите среднее значение, стандартное отклонение, максимальное и минимальное значение 
#для созданного ранее массива из 10 случайных целых чисел.
def f_7():
    arr = f_5()
    print("массив:", arr)
    print("среднее значение:", np.mean(arr))
    print("стандартное отклонение", np.std(arr))
    print("максимальное и минимальное значение", np.max(arr), np.min(arr))
    
