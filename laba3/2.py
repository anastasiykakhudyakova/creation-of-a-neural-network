# Импорт библиотеки numpy для работы с массивами и математическими операциями
import numpy as np

# Функция нейронной сети: вычисляет предсказание как скалярное произведение входов и весов
def neural_networks(inp, weights):
    return inp.dot(weights)

# Функция вычисления ошибки: квадрат разности между истинным значением и предсказанием
def get_error(true_predict, cur_predict):
    return (true_predict - cur_predict) ** 2

# Истинное значение, которое должна предсказать нейронная сеть
true_predict = 50

# Начальные веса нейронной сети (входных признаков два: [150, 40])
weights = np.array([0.2532, 0.3])

# Вычисление текущего предсказания нейронной сети
predict = neural_networks(np.array([150, 40]), weights)

# Вывод текущего предсказания
print(predict)

# Вывод текущей ошибки
print(get_error(true_predict, predict))
